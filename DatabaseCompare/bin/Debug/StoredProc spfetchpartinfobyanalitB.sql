
 PutovDrzava,
	po.PutovOstalo
FROM  dbo.Analitika a
	INNER JOIN dbo.PartnerStart ps on a.VezaID = ps.ID
	LE 

	LE 

	LEFT OUTER JOIN dbo.Kontakt k on kv.KontaktID = k.ID

	LEFT OUTER JOIN dbo.ParOsoba po on po.PartnerID = pa.PartnerID
	LEFT OUTER JOIN dbo.Drzava on dbo.Drzava.ID = po.PutovDrzavaID
WHERE a.ID = @inAnalitikaID AND a.AnalitikaVrsta = 2
ORDER BY k.ID 

END 

